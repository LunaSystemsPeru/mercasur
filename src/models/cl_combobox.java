package models;

/**
 *
 * @author luis
 */
public class cl_combobox {

    private int id;
    private String datos;

    public cl_combobox(int id, String datos) {
        this.id = id;
        this.datos = datos;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getDatos() {
        return datos;
    }

    public void setDatos(String datos) {
        this.datos = datos;
    }

    @Override
    public String toString() {
        return datos;
    }

    @Override
    public int hashCode() {
        int hash = 5;
        return hash;
    }

    @Override
    public boolean equals(Object obj) {
        if (obj == null) {
            return false;
        }
        if (getClass() != obj.getClass()) {
            return false;
        }
        final cl_combobox other = (cl_combobox) obj;
        if (this.id != other.id) {
            return false;
        }
        return true;
    }

}
