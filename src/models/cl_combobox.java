package models;

/**
 *
 * @author luis
 */
public class cl_combobox {

    private int id;
    private String datos;

    public cl_combobox(int id, String datos) {
        this.id = id;
        this.datos = datos;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getDatos() {
        return datos;
    }

    public void setDatos(String datos) {
        this.datos = datos;
    }

    @Override
    public String toString() {
        return datos;
    }

}
